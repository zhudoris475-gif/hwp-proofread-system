def find_context(line, pattern, window=30):
    idx = line.find(pattern)
    if idx == -1:
        return line
    start = max(0, idx - window)
    end = min(len(line), idx + len(pattern) + window)
    ctx = line[start:end]
    if start > 0:
        ctx = "..." + ctx
    if end < len(line):
        ctx = ctx + "..."
    return ctx

def is_jeomsa_jeok(text_after):
    if re.search(r'^적(으로|인|이|이다|적으로서|적이다)', text_after):
        return True
    return False

def is_yeonjeol_ende(text_before, text_after):
    if re.search(r'(하|하|되|있|없|좋|많|적|높|큰|작|편|나쁘|어렵|쉽|힘들|아프|가깝|멀|비슷|캄캄|소소|대단|산만|친절|달)$', text_before):
        if not re.search(r'^데[가로서에]', text_after):
            return True
    return False

