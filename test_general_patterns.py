# 오매치 테스트
print('\n=== False Positive Test ===')
fp_count = 0
for src, expected in danger_tests:
    result = src
    for pat, repl in patterns:
        try:
            new = re.sub(pat, repl, result)
            if new != result:
                result = new
                break
        except: pass
    if result != expected:
        fp_count += 1
        print(f'  FP: {src} -> {result} (expected: {expected})')
print(f'  False positives: {fp_count}/{len(danger_tests)}')

# 긍정 테스트
print('\n=== Positive Test ===')
ok = 0
fails = []
for src, expected in positive_tests:
    result = src
    for pat, repl in patterns:
        try:
            new = re.sub(pat, repl, result)
            if new != result:
                result = new
                break
        except: pass
    if result == expected: ok += 1
    else: fails.append((src, expected, result))
print(f'  Passed: {ok}/{len(positive_tests)}')
if fails:
    print('  FAILURES (need fixed-string rules):')
    for src, exp, got in fails:
        print(f'    {src}: expected={exp}, got={got}')
